package net.teamio.gtams.client;

public class GTamsException extends Exception {

	public GTamsException() {
	}

	public GTamsException(String arg0) {
		super(arg0);
	}

	public GTamsException(Throwable arg0) {
		super(arg0);
	}

	public GTamsException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GTamsException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
